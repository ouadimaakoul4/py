def add(x, y):
    return x + y


def multi(x, y):
    return x * y


def sou(x, y):
    return x - y


print("The sum of x and y is ", add(
    int(input("Please enter x : ")), int(input("Please enter y : "))))

print("The product of x and y is ", multi(
    int(input("Please enter x : ")), int(input("Please enter y :"))))

print("The difference of x and y is ", sou(
    int(input("Please enter x : ")), int(input("Please enter y : "))))
